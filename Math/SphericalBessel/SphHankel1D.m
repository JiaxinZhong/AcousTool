% ==========================================================================
% 计算球Hankel函数的微分
% --------------------------------------------------------------------------
% 输入
% 	n	--- 阶数；必须在第1维
%	z	--- 宗量；必须在第2维及以上
% --------------------------------------------------------------------------
% 输出
%	hd	---	球Hankel函数的微分
% ==========================================================================
function hd = SphHankel1D(max_order, z)
	z_row = z(:).';

	h = SphHankel1((0:max_order+1).', z_row);
    n = (0:max_order).';

	hd = n./z_row .* h(1:end-1,:) - h(2:end,:);

	hd = reshape(hd, size(n.*z));
end
